export type Pair<A, B> = {
    first: A,
    second: B
};

export type Triple<A, B, C> = {
    first: A,
    second: B,
    third: C
};

export type Quadruple<A, B, C, D> = {
    first: A,
    second: B,
    third: C,
    fourth: D
};

export type Quintuple<A, B, C, D, E> = {
    first: A,
    second: B,
    third: C,
    fourth: D,
    fifth: E
};